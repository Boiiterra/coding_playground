package vars

fun main() {
    // Constants values
    val str: String = "I am constant"
    val wow = "No need to type type"

    // Variables
    var integer: Int = 23
    var hello = "Bonjour"

    // Output options
    println("Constant values:")
    println("str -> $str")
    println("wow -> $wow")
    println("Variables:")
    println("integer -> $integer")
    println("hello -> $hello")
    println("integer times 7 is ${integer * 7}")
}
